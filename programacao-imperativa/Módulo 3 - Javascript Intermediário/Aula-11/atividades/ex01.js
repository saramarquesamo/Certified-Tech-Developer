//Escreva um aplicativo em Javascript que mostre todos os números ímpares de 1 até 100.
function passandoPeloPares() {
    for (let index = 0; index <= 100; index++) {
        if ((index % 2) == 0){
            console.log(index);
        }
    }
}
passandoPeloPares();
